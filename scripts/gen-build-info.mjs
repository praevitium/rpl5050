/* Generate www/src/build-info.js from package.json + git commit count.
   Runs as a `pre*` hook for serve/dev/build/bundle:vendor so every build
   pipeline picks up a fresh count without manual maintenance. */
import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');

const pkg = JSON.parse(readFileSync(resolve(root, 'package.json'), 'utf8'));
const version = pkg.version;

let build = '0';
try {
  build = execSync('git rev-list --count HEAD', { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
    .toString().trim() || '0';
} catch {
  // Not a git checkout (e.g. tarball install) — fall back to 0.
}

let sha = '';
try {
  sha = execSync('git rev-parse --short HEAD', { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
    .toString().trim();
} catch { /* ignore */ }

const full = `${version}-build${build}`;

const outPath = resolve(root, 'www/src/build-info.js');
const body = `/* AUTO-GENERATED by scripts/gen-build-info.mjs — do not edit by hand.
   Regenerated by every npm run that touches the build pipeline. */
export const VERSION = ${JSON.stringify(version)};
export const BUILD   = ${JSON.stringify(build)};
export const SHA     = ${JSON.stringify(sha)};
export const FULL    = ${JSON.stringify(full)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, body);
process.stdout.write(`build-info: ${full}${sha ? ` (${sha})` : ''}\n`);
